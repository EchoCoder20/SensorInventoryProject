public class IonizationSmokeSensor {
    private String detectionPrinciple;
    private String fastFlamingFireSensitivity;
    private Boolean interconnectivity;

    public String getDetectionPrinciple() {
        return detectionPrinciple;
    }

    public void setDetectionPrinciple(String detectionPrinciple) {
        this.detectionPrinciple = detectionPrinciple;
    }

    public String getFastFlamingFireSensitivity() {
        return fastFlamingFireSensitivity;
    }

    public void setFastFlamingFireSensitivity(String fastFlamingFireSensitivity) {
        this.fastFlamingFireSensitivity = fastFlamingFireSensitivity;
    }

    public Boolean getInterconnectivity() {
        return interconnectivity;
    }

    public void setInterconnectivity(Boolean interconnectivity) {
        this.interconnectivity = interconnectivity;
    }
}
