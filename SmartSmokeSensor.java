public class SmartSmokeSensor {
    private Boolean appNotification;
    private Boolean voiceAlerts;
    private Boolean coDetectorIntegration;

    public Boolean getAppNotification() {
        return appNotification;
    }

    public void setAppNotification(Boolean appNotification) {
        this.appNotification = appNotification;
    }

    public Boolean getVoiceAlerts() {
        return voiceAlerts;
    }

    public void setVoiceAlerts(Boolean voiceAlerts) {
        this.voiceAlerts = voiceAlerts;
    }

    public Boolean getCoDetectorIntegration() {
        return coDetectorIntegration;
    }

    public void setCoDetectorIntegration(Boolean coDetectorIntegration) {
        this.coDetectorIntegration = coDetectorIntegration;
    }
}
